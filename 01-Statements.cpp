/*
Um statement é uma instrução ou uma sequência de instruções em C++,
que faz com que o programa execute alguma ação.
A maioria (mas não todos) os statements em C++ termina em ponto-e-vírgula.
São a menor unidade de computação independente na linguagem C++.
Em uma linguagem de alto nível como C++, um único statement pode ser compilado em várias instruções de linguagem de máquina.

Existem muitos tipos diferentes de statements em C++:
Statements de declaração
Statements de jump
Statements de expressão
Statements compostas
Statements condicionais
Statements de iteração (loops)
Statements de blocos try

Os statements geralmente são agrupados em funções.
Uma função é uma coleção de instruções que são executadas sequencialmente.
*/

